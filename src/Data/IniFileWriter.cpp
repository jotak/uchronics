#include "IniFileWriter.h"

// -----------------------------------------------------------------
// Name : IniFileWriter
// -----------------------------------------------------------------
IniFileWriter::IniFileWriter()
{
}

// -----------------------------------------------------------------
// Name : ~IniFileWriter
// -----------------------------------------------------------------
IniFileWriter::~IniFileWriter()
{
}

// -----------------------------------------------------------------
// Name : set
// -----------------------------------------------------------------
void IniFileWriter::set(string sKey, string value)
{
	stream << sKey << " = " << value << endl;
}

// -----------------------------------------------------------------
// Name : set
// -----------------------------------------------------------------
void IniFileWriter::set(string sKey, bool value)
{
	stream << sKey << " = " << value << endl;
}

// -----------------------------------------------------------------
// Name : set
// -----------------------------------------------------------------
void IniFileWriter::set(string sKey, int value)
{
	stream << sKey << " = " << value << endl;
}

// -----------------------------------------------------------------
// Name : set
// -----------------------------------------------------------------
void IniFileWriter::set(string sKey, float value)
{
	stream << sKey << " = " << value << endl;
}

// -----------------------------------------------------------------
// Name : write
// -----------------------------------------------------------------
bool IniFileWriter::write(string sFileName)
{
    ofstream file(sFileName.c_str());
    if (!file.is_open()) {
        return false;
    }

    file << stream.str();
    file.close();
    return true;
}
